    private void clientStreamBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clientStreamBtnActionPerformed
        // TODO add your handling code here:
        String location = lightLocField.getText();
        try {
            if (location.isEmpty()) {
                throw new IllegalArgumentException("Location cannot be empty");
            }
            ManagedChannel registryChannel = ManagedChannelBuilder.forAddress("localhost", 50050).usePlaintext().build();
            RegistryServiceGrpc.RegistryServiceBlockingStub registryStub = RegistryServiceGrpc.newBlockingStub(registryChannel);

            ServiceResponse res = registryStub.getService(ServiceRequest.newBuilder().setServiceName("streetlight").build());
            String[] parts = res.getAddress().split(":");

            ManagedChannel channel = ManagedChannelBuilder.forAddress(parts[0], Integer.parseInt(parts[1])).usePlaintext().build();

            StreetLightControlServiceGrpc.StreetLightControlServiceStub stub
                    = attachJwt(StreetLightControlServiceGrpc.newStub(channel));

            CountDownLatch latch = new CountDownLatch(1);

            StreamObserver<SetBrightnessRequest> requestObserver = stub.adjustMultipleLights(
                    new StreamObserver<SetBrightnessResponse>() {
                public void onNext(SetBrightnessResponse value) {
                    resultArea.append("[Client Stream] \nAdjusting Lights in: " + location + " \n" + value.getMessage() + "\n");
                }

                public void onError(Throwable t) {
                    resultArea.append("Client stream error: " + t.getMessage() + "\n");
                    latch.countDown();
                }

                public void onCompleted() {
                    resultArea.append("Client streaming completed.\n");
                    latch.countDown();
                }
            });

            requestObserver.onNext(SetBrightnessRequest.newBuilder().setLightID("LIGHT_1").setBrightnessLevel((int) (Math.random() * 100)).build());
            requestObserver.onNext(SetBrightnessRequest.newBuilder().setLightID("LIGHT_2").setBrightnessLevel((int) (Math.random() * 100)).build());
            requestObserver.onNext(SetBrightnessRequest.newBuilder().setLightID("LIGHT_3").setBrightnessLevel((int) (Math.random() * 100)).build());
            requestObserver.onNext(SetBrightnessRequest.newBuilder().setLightID("LIGHT_4").setBrightnessLevel((int) (Math.random() * 100)).build());
            requestObserver.onNext(SetBrightnessRequest.newBuilder().setLightID("LIGHT_5").setBrightnessLevel((int) (Math.random() * 100)).build());
            requestObserver.onCompleted();

            latch.await();
            channel.shutdown();

        } catch (IllegalArgumentException e) {
            resultArea.append("Error in adjust multiple lights: INVALID_ARGUMENT: " + e.getMessage()+"\n");
        } catch (Exception ex) {
            resultArea.append("Error in adjust multiple lights: " + ex.getMessage() + "\n");
        }
    }//GEN-LAST:event_clientStreamBtnActionPerformed

