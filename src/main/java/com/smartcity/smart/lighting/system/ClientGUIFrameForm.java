    private void setBrightnessBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setBrightnessBtnActionPerformed
        // TODO add your handling code here:
        String lightID = lightIDField.getText();
        String location = lightLocField.getText();
        try {
            if (location.isEmpty()) {
                throw new IllegalArgumentException("Location cannot be empty");
            }
            ManagedChannel registryChannel = ManagedChannelBuilder.forAddress("localhost", 50050).usePlaintext().build();
            RegistryServiceGrpc.RegistryServiceBlockingStub registryStub = RegistryServiceGrpc.newBlockingStub(registryChannel);

            ServiceResponse res = registryStub.getService(ServiceRequest.newBuilder().setServiceName("streetlight").build());
            String[] parts = res.getAddress().split(":");

            ManagedChannel channel = ManagedChannelBuilder.forAddress(parts[0], Integer.parseInt(parts[1])).usePlaintext().build();

            StreetLightControlServiceGrpc.StreetLightControlServiceBlockingStub stub
                    = attachJwt(StreetLightControlServiceGrpc.newBlockingStub(channel));

            SetBrightnessResponse response = stub.setBrightness(
                    SetBrightnessRequest.newBuilder()
                            .setLightID(lightID)
                            .setBrightnessLevel(70)
                            .build()
            );

            resultArea.append("[Unary] Set brightness for " + location + ", " + lightID + ": " + response.getStatus() + "\n");

        } catch (IllegalArgumentException e) {
            resultArea.append("Error setting brightness: INVALID_ARGUMENT: " + e.getMessage()+"\n");

        } catch (Exception ex) {
            resultArea.append("Error setting brightness: " + ex.getMessage() + "\n");
        }
    }//GEN-LAST:event_setBrightnessBtnActionPerformed

